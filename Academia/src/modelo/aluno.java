package modelo;

public class aluno {
    int id;
    String nome;
    String cpf;
    int idade;
    float mensalidade;
    float multaporatraso;
    String endereco;
    String telefone;
    float plano;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public float getMensalidade() {
        return mensalidade;
    }

    public void setMensalidade(float mensalidade) {
        this.mensalidade = mensalidade;
    }

    public float getMultaporatraso() {
        return multaporatraso;
    }

    public void setMultaporatraso(float multaporatraso) {
        this.multaporatraso = multaporatraso;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public float getPlano() {
        return plano;
    }

    public void setPlano(float plano) {
        this.plano = plano;
    }
}
